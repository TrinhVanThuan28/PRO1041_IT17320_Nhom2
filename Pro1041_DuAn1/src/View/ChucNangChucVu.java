    public void loadForm() {
        table = (DefaultTableModel) tb_bangChucVu.getModel();
        table.setRowCount(0);
        for (ChucVu cv : this.cvs.getlist()) {
            table.addRow(new Object[]{cv.getId(), cv.getMa(), cv.getTen()});
        }
    }
    public void clearForm(){
        lb_id.setText("");
        txt_ma.setText("");
        txt_ten.setText("");
    }

    public ChucVu GetForm() {
        String idStr = lb_id.getText().trim();
        String maStr = txt_ma.getText().trim();
        String tenStr = txt_ten.getText().trim();
        if (idStr.length() == 0 || maStr.length() == 0 || tenStr.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được để trống thông tin");
            return null;
        }
        ChucVu cv = new ChucVu(idStr, maStr, tenStr);
        return cv;
    }

