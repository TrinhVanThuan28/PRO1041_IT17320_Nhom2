    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String maStr = lb_id.getText().trim();
        if (cvs.checkMa(maStr) != null) {
            JOptionPane.showMessageDialog(this, "Ko thêm được trùng mã");
            return;

        } else {
            ChucVu cv = this.GetForm();
            if (cv == null) {
                return;
            }
            this.cvs.add(cv);
            loadForm();
        }
        clearForm();
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tb_bangChucVu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa");
            return;
        }
        String idStr = tb_bangChucVu.getValueAt(row, 0).toString();
        ChucVu cv = this.GetForm();
        if (cv == null) {
            return;
        }
        this.cvs.sua(cv, idStr);
        loadForm();
        clearForm();
    }//GEN-LAST:event_btn_suaActionPerformed

    private void tb_bangChucVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_bangChucVuMouseClicked
        // TODO add your handling code here:
        int row = tb_bangChucVu.getSelectedRow();
        if (row == -1) {
            return;
        }
        lb_id.setText((String) tb_bangChucVu.getValueAt(row, 0).toString());
        txt_ma.setText((String) tb_bangChucVu.getValueAt(row, 1).toString());
        txt_ten.setText((String) tb_bangChucVu.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tb_bangChucVuMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
         int row = tb_bangChucVu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xoá");
            return;
        }
        String idStr = tb_bangChucVu.getValueAt(row, 0).toString();
        int check = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muỗn xoá dòng này chứ");
        if(check != JOptionPane.YES_OPTION){
            return;
            }
        cvs.xoa(idStr);
        loadForm();
        clearForm();
    }//GEN-LAST:event_btn_xoaActionPerformed

