    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int row = tb_mausac.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xoá");
            return;
        }
        int check = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xoá dòng này?");
        if (check != JOptionPane.YES_OPTION) {
            return;
        }
        String idStr = tb_mausac.getValueAt(row, 0).toString();
        this.MSService.xoa(idStr);
        addBang();
        clearForm();
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tb_mausac.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa ");
            return;
        }

        MauSac mS = this.getFrom();
        if (mS == null) {
            return;
        }
        String idStr = tb_mausac.getValueAt(row, 0).toString();
        this.MSService.sua(mS, idStr);
        addBang();
        clearForm();
    }//GEN-LAST:event_btn_suaActionPerformed

    private void tb_mausacMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_mausacMouseClicked
        // TODO add your handling code here:
        int row = tb_mausac.getSelectedRow();
        if (row == -1) {
            return;
        }
        lbl_id.setText((String) tb_mausac.getValueAt(row, 0).toString());
        txt_ma.setText((String) tb_mausac.getValueAt(row, 1).toString());
        txt_ten.setText((String) tb_mausac.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tb_mausacMouseClicked

    private void btn_timKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timKiemActionPerformed
        // TODO add your handling code here:
        String tenStr = cbb_Ten.getSelectedItem().toString();
        table.setRowCount(0);
        for (MauSac n : this.MSService.searchByTen(tenStr)) {
            table.addRow(new Object[]{n.getIdMau(), n.getMaMau(), n.getTenMau()});
        }
        fillTable();
        JOptionPane.showMessageDialog(this, "Tìm kiếm thành công");
    }//GEN-LAST:event_btn_timKiemActionPerformed
    public void clearForm() {
        lbl_id.setText("-");
        txt_ma.setText("");
        txt_ten.setText("");
    }

    public void fillTable() {
        String tenStr = cbb_Ten.getSelectedItem().toString();
        for (MauSac mS : this.MSService.searchByTen(tenStr)) {
            lbl_id.setText(mS.getIdMau());
            txt_ma.setText(mS.getMaMau());
            txt_ten.setText(mS.getTenMau());
        }
    }

