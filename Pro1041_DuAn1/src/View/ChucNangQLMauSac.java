    public void addBang() {
        table = (DefaultTableModel) tb_mausac.getModel();
        table.setRowCount(0);
        for (MauSac n : this.MSService.getlist()) {
            table.addRow(new Object[]{n.getIdMau(), n.getMaMau(), n.getTenMau()});
        }
    }

    public void addComBoBox(ArrayList<MauSac> list) {
        combo = (DefaultComboBoxModel) cbb_Ten.getModel();
        for (MauSac mS : list) {
            combo.addElement(mS);
        }
    }

    public MauSac getFrom() {
        String idStr = lbl_id.getText().trim();
        String maStr = txt_ma.getText().trim();
        String tenStr = txt_ten.getText().trim();
        if (idStr.length() == 0 || maStr.length() == 0 || tenStr.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được để trống thông tin");
            return null;
        }
        MauSac mS = new MauSac(idStr, maStr, tenStr);
        return mS;
    }

