    public void addBang() {
        table = (DefaultTableModel) tb_chatlieu.getModel();
        table.setRowCount(0);
        for (ChatLieu n : this.CLService.getlist()) {
            table.addRow(new Object[]{n.getId(), n.getMa(), n.getTen()});
        }
    }

    public void addComBoBox(ArrayList<ChatLieu> list) {
        combo = (DefaultComboBoxModel) cbb_Ten.getModel();
        for (ChatLieu cL : list) {
            combo.addElement(cL);
        }
    }

    public ChatLieu getFrom() {
        String idStr = lbl_id.getText().trim();
        String maStr = txt_ma.getText().trim();
        String tenStr = txt_ten.getText().trim();
        if (idStr.length() == 0 || maStr.length() == 0 || tenStr.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được để trống thông tin");
            return null;
        }
        ChatLieu cL = new ChatLieu(idStr, maStr, tenStr);
        return cL;
    }

    public void clearForm() {
        lbl_id.setText("-");
        txt_ma.setText("");
        txt_ten.setText("");
    }

    public void fillTable() {
        String tenStr = cbb_Ten.getSelectedItem().toString();
        for (ChatLieu cL : this.CLService.searchByTen(tenStr)) {
            lbl_id.setText(cL.getId());
            txt_ma.setText(cL.getMa());
            txt_ten.setText(cL.getTen());
        }
    }

