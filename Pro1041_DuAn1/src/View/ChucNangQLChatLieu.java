    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        String tenStr = cbb_Ten.getSelectedItem().toString();
        table.setRowCount(0);
        for (ChatLieu n : this.CLService.searchByTen(tenStr)) {
            table.addRow(new Object[]{n.getId(), n.getMa(), n.getTen()});
        }
        fillTable();
        JOptionPane.showMessageDialog(this, "Tìm kiếm thành công");
    }//GEN-LAST:event_btn_timkiemActionPerformed

    private void tb_chatlieuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_chatlieuMouseClicked
        // TODO add your handling code here:
        int row = tb_chatlieu.getSelectedRow();
        if (row == -1) {
            return;
        }
        lbl_id.setText((String) tb_chatlieu.getValueAt(row, 0).toString());
        txt_ma.setText((String) tb_chatlieu.getValueAt(row, 1).toString());
        txt_ten.setText((String) tb_chatlieu.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tb_chatlieuMouseClicked

