    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        if (theLoaiSV.checkTrungMa(txt_maTL.getText().trim()) != null) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được trùng mã");
            return;
        }else{
             TheLoai tl = this.getFrom();
        if (tl == null) {
            return;
        }
        this.theLoaiSV.them(tl);
        addBang();
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        int row = tb_theLoai.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa ");
            return;
        }
        TheLoai tl = this.getFrom();
        if (tl == null) {
            return;
        }
        String idStr = tb_theLoai.getValueAt(row, 0).toString();
        this.theLoaiSV.sua(tl, idStr);
        addBang();
    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int row = tb_theLoai.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xoá");
            return;
        }
        int check = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xoá dòng này?");
        if (check != JOptionPane.YES_OPTION) {
            return;
        }
        String idStr = tb_theLoai.getValueAt(row, 0).toString();
        this.theLoaiSV.xoa(idStr);
        addBang();
        clearForm();
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void tb_theLoaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_theLoaiMouseClicked
        int row = tb_theLoai.getSelectedRow();
        if (row == -1) {
            return;
        }
        lb_id.setText((String) tb_theLoai.getValueAt(row, 0).toString());
        txt_maTL.setText((String) tb_theLoai.getValueAt(row, 1).toString());
        txt_loaiCo.setText((String) tb_theLoai.getValueAt(row, 2).toString());
        txt_loaiAo.setText((String) tb_theLoai.getValueAt(row, 3).toString());
        txt_luaTuoi.setText((String) tb_theLoai.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tb_theLoaiMouseClicked

