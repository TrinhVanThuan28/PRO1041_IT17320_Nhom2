    public void addBang() {
        dtm = (DefaultTableModel) tb_theLoai.getModel();
        dtm.setRowCount(0);
        for (TheLoai tl : this.theLoaiSV.getlist()) {
            dtm.addRow(new Object[]{tl.getId(), tl.getMaTL(), tl.getLoaiCo(), tl.getLoaiAo(), tl.getLuaTuoi()});
        }
    }

    public TheLoai getFrom() {
        String idStr = lb_id.getText().trim();
        String maStr = txt_maTL.getText().trim();
        String loaiCoStr = txt_loaiCo.getText().trim();
        String loaiAoStr = txt_loaiAo.getText().trim();
        String luaTuoiStr = txt_luaTuoi.getText().trim();
        if (idStr.length() == 0 || maStr.length() == 0 || loaiCoStr.length() == 0 || loaiAoStr.length() == 0 || luaTuoiStr.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được để trống thông tin");
            return null;
        }
        TheLoai tl = new TheLoai(idStr, maStr, loaiCoStr, loaiAoStr, luaTuoiStr);
        return tl;
    }

